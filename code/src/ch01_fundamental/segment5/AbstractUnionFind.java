package ch01_fundamental.segment5;

public abstract class AbstractUnionFind implements UnionFind {

    protected int[] id;
    protected int count;

    public AbstractUnionFind(int n) {
        count = n;
        id = new int[n];
        for (int i = 0; i < n; i++) {
            id[i] = i;
        }
    }

    @Override
    public boolean connected(int p, int q) {
        return find(p) == find(q);
    }

    @Override
    public int count() {
        return count;
    }
}
